dokładnie to koszem i siatką. Lubi ona również długo spać i pisać esy. Nie daleko Kasi mieszka Dominik i prawie jak każdy lubi Internet. Nasz kolega zazwyczaj spędza czas z przyjaciółmi i gra w piłkę nożną. Gdy nadchodzi lato przyjeżdża do szkoły na rowerze, który jest pasję Fabiana. Ostatnim chłopakiem w naszej klasie jest Bartosz Stęszewski, który lubi przerabiać komputery i telefony.</p>
			<p>Pierwszą dziewczyną w dzienniku jest Marta uwielbiająca białą czekoladę i koty. W wolnym czasie ogląda TV i spotyka się ze znajomymi. Jednak chyba najwięcej czasu z przyjaciółmi lubi spędzać Magda. Wśród dziewczyn z naszej super klasy, książkę zazwyczaj możemy zobaczyć w rękach Zuzi ponieważ jak Bercik lubi czytać. </p>
			<p>Z całego klasowego grona „najnowsza” jest Monika ponieważ całkiem od nie dawna, a dokładnie od 08.03.2011 roku chodzi do naszej szkoły. Zostały jeszcze dwie dziewczyny, którymi są Natalia i Angelika. Zainteresowania Natalii to:  ekologia, historia, kultura, nauka, fotografia, podróże, ludzie oraz ich kultura, motoryzacja, medycyna, archeologia, przyroda, muzyka, kino i literatura. Najbardziej jednak nasza koleżanka lubi słuchać muzyki, oglądać filmy i spotykać się z przyjaciółmi. Robić to lubi także Angelika tylko, że do tego dochodzą jeszcze psy, motory i pomoc ludziom. No i na koniec Pani Janina Mazur- nasza wychowawczyni. Bez niej w klasie zazwyczaj panuje chaos. Nasza pani lubi święty spokój, wędrować po górach, zwiedzać zamki, jeździć konno, uprawiać sporty letnie i zimowe, a osobą, której wychowawczyni nie lubi to dentysta. Wcześniej jak byliśmy jeszcze uczniami klasy pierwszej chodziły do nas cztery osoby: Daria, Ala, Piotrek (Brutus) i Klaudia, która z początkiem klasy trzeciej do nas wróciła. Pasją naszej koleżanki jest siatkówka.</p>
